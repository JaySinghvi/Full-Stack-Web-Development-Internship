/* 
local package:
1. installed in directory using: npm install <package-name> by placing codes under node-modules
2. import to code by: require('package-name')
3. can have different version
4. occupies more memory
5. used to execute any type of command

global package:
1. installed in single place using: npm install -g <package-name>
2. imported to code by require('package-name')
3. can have single version of same package
4. occupies less memory
5. used to execute only shell commands

all the packages can be found on npm js org website
*/