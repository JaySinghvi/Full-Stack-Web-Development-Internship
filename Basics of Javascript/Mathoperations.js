var num1 = 3.5
var ceil_value = Math.ceil(num1)//ceil is used to round off the values to highest no. like 3.2 or 3.5 will be rounded to 4
console.log("round of value = " +ceil_value)

var floor_value = Math.floor(num1)//floor is used to round off the values to lowest no. like 3.2 or 3.5 will be rounded to 3
console.log("round of value = " +floor_value)

var num2 = 16
console.log("sqrt of num2 = " +Math.sqrt(num2))//used to find sqrt of a no.

console.log("2^3 = " +Math.pow(2, 3))//used to find power of some no.

console.log("pi value = " +Math.PI)//gives the value of pi

