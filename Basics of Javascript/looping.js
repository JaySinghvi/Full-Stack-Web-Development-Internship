/*2 types of loops:
1. for loop: used when we know the no. of iterations
2. while loop: used when we don't know the no. of iterations and check conditions first and then executes the instructions
3. do while loop: first it executes the instructions and then check the condition*/

//syntax for loop: same like c program
syntax: for(var count = 0; count < 10; count++)
console.log("count = " +count)

//syntax for while loop
var counter = 10;
while(counter<10){
    console.log("counter = " +counter)
    counter++
}


